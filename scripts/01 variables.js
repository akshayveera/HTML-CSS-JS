

// variables (also known as identifiers)

// 2 types mutable, immutable
// mutable - let
// immutable - const


// mutable

let x;  // declaration
x = 9;  // initialisation

let y = 10;  // declaration and initialisation

// we can also use var keyword to define mutable variable but we generally dont use it

// immutable

// we should do both declaration and initialisation at a time in case of immutable

const z = 26;


console.log("x : ",x);
console.log("y : ",y);
console.log("z : ",z);


